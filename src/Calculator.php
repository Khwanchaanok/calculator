<?php

namespace Khwanchanok\Calculator;

class Calculator
{
    public function add($first_num , $secound_num)
    {
        return $first_num + $secound_num;
    }
     public function subtrack($first_num , $secound_num)
    {
        return $first_num - $secound_num;
    }
     public function multiply($first_num , $secound_num)
    {
        return $first_num * $secound_num;
    }
     public function devide($first_num , $secound_num)
    {
        return $first_num / $secound_num;
    }
}
